package com.itwill.staily.mypage.model.dto;

public class Payment {
	private int py_no;
	private int m_no;
	private int py_price;
	private String py_date;
	private String py_name;
	private String py_period;
	private String py_type;
	
	public Payment() {
		
	}
	
	public int getPy_no() {
		return py_no;
	}
	public void setPy_no(int py_no) {
		this.py_no = py_no;
	}
	public int getM_no() {
		return m_no;
	}
	public void setM_no(int m_no) {
		this.m_no = m_no;
	}
	public int getPy_price() {
		return py_price;
	}
	public void setPy_price(int py_price) {
		this.py_price = py_price;
	}
	public String getPy_date() {
		return py_date;
	}
	public void setPy_date(String py_date) {
		this.py_date = py_date;
	}
	public String getPy_name() {
		return py_name;
	}
	public void setPy_name(String py_name) {
		this.py_name = py_name;
	}
	public String getPy_period() {
		return py_period;
	}
	public void setPy_period(String py_period) {
		this.py_period = py_period;
	}
	public String getPy_type() {
		return py_type;
	}
	public void setPy_type(String py_type) {
		this.py_type = py_type;
	}
	
	
}
